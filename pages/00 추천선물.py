import streamlit as st

# 앱 기본 설정
st.set_page_config(page_title="여름 헤어스타일 & 선물 추천", page_icon="🌞", layout="centered")

# 타이틀 및 소개
st.title("🌴 여름엔 무슨 헤어가 어울릴까? 💇‍♀️💇‍♂️")
st.markdown("""
### ☀️ 뜨거운 여름, 스타일도 바뀌어야지!
어울리는 **헤어스타일**, 분위기 **무드 추천**, 그리고 기분 좋은 **여름 선물**까지 준비했어요!  
**얼굴형**을 선택하고, 나만의 여름 무드를 찾아보세요. 🍧🌊
""")

# 얼굴형 선택
st.subheader("📐 내 얼굴형은?")
face_shape = st.selectbox("얼굴형을 선택해주세요 👇", [
    "둥근형 😊", "계란형 🥚", "각진형 🟥", "긴형 😌", "하트형 💖"
])

# 얼굴형별 헤어스타일 추천
st.markdown("### 💇‍♀️ 어울리는 여름 헤어스타일은?")
if face_shape == "둥근형 😊":
    st.success("👉 시스루 뱅, 웨이브 단발, C컬 단발 💕\n\n시원하게 턱 라인을 강조해보세요!")
elif face_shape == "계란형 🥚":
    st.success("👉 어떤 스타일도 찰떡! 긴 생머리, 레이어드 컷, 뿌리 볼륨 추천 🌸")
elif face_shape == "각진형 🟥":
    st.success("👉 레이어드 펌, 볼륨 단발, 사이드 뱅 💁‍♀️\n\n부드럽게 각진 라인을 커버해보세요.")
elif face_shape == "긴형 😌":
    st.success("👉 풀뱅 단발, 히피펌, 쉐기컷 ✨\n\n얼굴 길이를 줄여주는 효과 굿!")
elif face_shape == "하트형 💖":
    st.success("👉 하프업, 가르마펌, 물결 웨이브 🌷\n\n턱이 갸름해 보이는 스타일로!")

# 분위기 선택
st.subheader("🎀 원하는 분위기는?")
mood = st.radio("원하는 여름 분위기를 골라보세요! 🌈", ["청순 ☁️", "힙한 🔥", "러블리 💗", "내추럴 🌿", "섹시 💋"])

# 분위기별 컨셉 제안
st.markdown("### 🎨 이런 무드 어때요?")
if mood == "청순 ☁️":
    st.info("화이트 원피스, 내추럴 웨이브, 투명 메이크업 🌸\n\n☁️ 시원하고 깨끗한 이미지로 변신!")
elif mood == "힙한 🔥":
    st.info("버킷햇, 크롭티, 애쉬 컬러 헤어 🎧\n\n🔥 스트릿 감성으로 여름을 정복!")
elif mood == "러블리 💗":
    st.info("핑크 계열 틴트, 리본 액세서리, 웨이브 반묶음 💕\n\n💗 귀여움은 여름에도 필수!")
elif mood == "내추럴 🌿":
    st.info("린넨 셔츠, 플랫 슈즈, 민낯 메이크업 🌾\n\n🌿 편안하고 꾸안꾸 스타일!")
elif mood == "섹시 💋":
    st.info("레드립, 블랙 드레스, 젖은 머리 연출 💄\n\n💋 여름밤, 매혹적인 분위기로!")

# 선물 추천
st.subheader("🎁 여름 선물 추천")
st.markdown("""
| 분위기 | 추천 선물 🎁 |
|--------|--------------|
| 청순 ☁️ | 민트향 바디미스트 💧, 린넨 파우치 🎐 |
| 힙한 🔥 | 블루투스 스피커 🎵, 그래픽 티셔츠 👕 |
| 러블리 💗 | 하트 파우치 🫶, 젤 네일 키트 💅 |
| 내추럴 🌿 | 허브티 세트 🍵, 라탄 가방 👜 |
| 섹시 💋 | 향수 💃, 레드립 틴트 💄 |
""")

